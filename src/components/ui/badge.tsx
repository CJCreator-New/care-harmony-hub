import * as React from "react";
import { cva, type VariantProps } from "class-variance-authority";

import { cn } from "@/lib/utils";

const badgeVariants = cva(
  "inline-flex items-center rounded-full border px-2.5 py-0.5 text-xs font-semibold transition-colors focus:outline-none focus:ring-2 focus:ring-ring focus:ring-offset-2",
  {
    variants: {
      variant: {
        default: "border-transparent bg-primary text-primary-foreground hover:bg-primary/80",
        secondary: "border-transparent bg-secondary text-secondary-foreground hover:bg-secondary/80",
        destructive: "border-transparent bg-destructive text-destructive-foreground hover:bg-destructive/80",
        outline: "text-foreground",
        success: "border-transparent bg-green-500/10 text-green-600",
        warning: "border-transparent bg-amber-500/10 text-amber-600",
        info: "border-transparent bg-blue-500/10 text-blue-600",
        emergency: "border-transparent bg-red-500/10 text-red-600 animate-pulse",
        urgent: "border-transparent bg-orange-500/10 text-orange-600",
        normal: "border-transparent bg-blue-500/10 text-blue-600",
        low: "border-transparent bg-slate-500/10 text-slate-600",
        doctor: "border-transparent bg-blue-500/10 text-blue-600",
        nurse: "border-transparent bg-green-500/10 text-green-600",
        pharmacy: "border-transparent bg-purple-500/10 text-purple-600",
        receptionist: "border-transparent bg-orange-500/10 text-orange-600",
        admin: "border-transparent bg-red-500/10 text-red-600",
        patient: "border-transparent bg-teal-500/10 text-teal-600",
      },
    },
    defaultVariants: {
      variant: "default",
    },
  },
);

export interface BadgeProps extends React.HTMLAttributes<HTMLDivElement>, VariantProps<typeof badgeVariants> {}

const Badge = React.forwardRef<HTMLDivElement, BadgeProps>(
  ({ className, variant, ...props }, ref) => {
    return <div ref={ref} className={cn(badgeVariants({ variant }), className)} {...props} />;
  }
);
Badge.displayName = "Badge";

export { Badge, badgeVariants };
